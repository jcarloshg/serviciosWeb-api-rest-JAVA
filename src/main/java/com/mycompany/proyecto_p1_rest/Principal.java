    private void actulizarProducto(Detail detail, java.awt.event.ActionEvent event){
        int reply = JOptionPane.showConfirmDialog(
            this, "¬øSeguro quiere actualizar el producto?",
            "", JOptionPane.YES_NO_OPTION
        );
        
        if( reply == JOptionPane.YES_OPTION)
        {
            ResponseUpdateProd res = conection.updateProd(user, pass, detail);
            int typeMessa = (res.getCode() == 203 ) 
                    ? JOptionPane.INFORMATION_MESSAGE : JOptionPane.ERROR_MESSAGE;
            
            showMessage(String.valueOf(res.getCode()), res.getMessage(), typeMessa);
            
            if(res.getCode() == 203)
            {
                changeCard(jLayeredPane2, operations);
                searchIsbnBtnActionPerformed(event);
                clearForm();
            }
        }
    }
    
