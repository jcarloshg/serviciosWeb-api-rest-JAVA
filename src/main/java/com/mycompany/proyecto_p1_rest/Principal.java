    private void searchIsbnBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchIsbnBtnActionPerformed

        if(isbnTextField.getText().length() > 0)
        {
            resProdDetails = conection.getDetails(user, pass, isbnTextField.getText());
                    
            if (resProdDetails.isComplete()) 
            {
                proDetails = resProdDetails.getDetail();
                String info = "";
                info += "IBNS           " + proDetails.getIsbn() + "\n";
                info += "Nombre         " + proDetails.getName() + "\n";
                info += "Author         " + proDetails.getAuthor() + "\n";
                info += "Editorial      " + proDetails.getEditorial() + "\n";
                info += "Precio         " + proDetails.getPrice() + "\n";
                info += "A√±o            " + proDetails.getYear() + "\n";
                info += "Oferta         " + String.valueOf(resProdDetails.getOffer());
                detailTextArea.setText(info);
                changeCard(operationsLayeredPane, detailProdJPanel);
                changeState(
                        String.valueOf(resProdDetails.getCode()), 
                        resProdDetails.getMessage());
            }
            else
            {
                isbnTextField.setText("");
                showMessage(
                        String.valueOf(resProdDetails.getCode()), 
                        resProdDetails.getMessage(),
                        JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            showMessage("-1", 
                    "El cuadrode texto ISBN esta vacio", 
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_searchIsbnBtnActionPerformed

