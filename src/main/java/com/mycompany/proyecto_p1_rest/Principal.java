    private void searchCategoryBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchCategoryBtnActionPerformed
        if( categoryTextField.getText().length() > 0)
        {
            ResponseGetProd res = conection.getProd(user, pass, categoryTextField.getText());
        
            if (res.isComplete()) {
                ArrayList<Product> products = res.getProducts();
                String data = "";
            
                for (Product product : products) 
                    data += product.getIsbn() + "\t" + product.getName() + "\n";
            
                prodsCateTextArea.setText(data);
                System.out.println(data);
                changeCard(operationsLayeredPane, prodsCateJPanel);
            }
            else
            {
                categoryTextField.setText("");
                JOptionPane.showMessageDialog(
                    this, String.valueOf(res.getCode())+" "+res.getMessage(), 
                    res.getStatus(), JOptionPane.ERROR_MESSAGE);
            }
            changeState(String.valueOf(res.getCode()), res.getMessage());
        }
        else
        {
            changeState("1", "Cuadro de texto CATEGORIA vacio");
            JOptionPane.showMessageDialog(
                    this, "Cuadro de texto CATEGORIA vacio", 
                    "", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_searchCategoryBtnActionPerformed

