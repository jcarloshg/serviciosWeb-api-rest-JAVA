    private void searchIsbnBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchIsbnBtnActionPerformed

        if(isbnTextField.getText().length() > 0)
        {
            resProdDetails = conection.getDetails(user, pass, isbnTextField.getText());
                    
            if (resProdDetails.isComplete()) 
            {
                proDetails = resProdDetails.getDetail();
                String info = "";
                info += "IBNS           " + proDetails.getIsbn() + "\n";
                info += "Nombre         " + proDetails.getName() + "\n";
                info += "Author         " + proDetails.getAuthor() + "\n";
                info += "Editorial      " + proDetails.getEditorial() + "\n";
                info += "Precio         " + proDetails.getPrice() + "\n";
                info += "A√±o            " + proDetails.getYear() + "\n";
                info += "Oferta         " + String.valueOf(resProdDetails.getOffer());
                detailTextArea.setText(info);
                changeCard(operationsLayeredPane, detailProdJPanel);
                changeState(
                        String.valueOf(resProdDetails.getCode()), 
                        resProdDetails.getMessage());
            }
            else
            {
                isbnTextField.setText("");
                showMessage(
                        String.valueOf(resProdDetails.getCode()), 
                        resProdDetails.getMessage(),
                        JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            showMessage("-1", 
                    "El cuadrode texto ISBN esta vacio", 
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_searchIsbnBtnActionPerformed

    private void searchCategoryBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchCategoryBtnActionPerformed
        if( categoryTextField.getText().length() > 0)
        {
            ResponseGetProd res = conection.getProd(user, pass, categoryTextField.getText());
        
            if (res.isComplete()) {
                ArrayList<Product> products = res.getProducts();
                String data = "";
            
                for (Product product : products) 
                    data += product.getIsbn() + "\t" + product.getName() + "\n";
            
                prodsCateTextArea.setText(data);
                System.out.println(data);
                changeCard(operationsLayeredPane, prodsCateJPanel);
            }
            else
            {
                categoryTextField.setText("");
                JOptionPane.showMessageDialog(
                    this, String.valueOf(res.getCode())+" "+res.getMessage(), 
                    res.getStatus(), JOptionPane.ERROR_MESSAGE);
            }
            changeState(String.valueOf(res.getCode()), res.getMessage());
        }
        else
        {
            changeState("1", "Cuadro de texto CATEGORIA vacio");
            JOptionPane.showMessageDialog(
                    this, "Cuadro de texto CATEGORIA vacio", 
                    "", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_searchCategoryBtnActionPerformed

