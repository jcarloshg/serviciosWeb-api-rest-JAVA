    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
      
        stateForm = StateForm.UPDATE_FORM;
        if( proDetails != null)
        {
            titleFormLabel.setText("Actualizar producto");
            
            switch(proDetails.getIsbn().substring(0, 3))
            {
                case "CMS" : categoFormComBox.setSelectedIndex(0); break;
                case "MGS" : categoFormComBox.setSelectedIndex(1); break;
                case "LBS" : categoFormComBox.setSelectedIndex(2); break;
            }
            isbnFormTextField.setText(proDetails.getIsbn().substring(3));
            nameFormTextField.setText(proDetails.getName());
            authorFormTextField.setText(proDetails.getAuthor());
            editorialFormTextField.setText(proDetails.getEditorial());
            yearFormTextField.setText(String.valueOf(proDetails.getYear()));
            priceFormTextField.setText(proDetails.getPrice());

            changeCard(jLayeredPane2, FormProduct);
        }
        else
        {
            JOptionPane.showMessageDialog(
                this, "Ning√∫n producto seleccionado",
                "", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

